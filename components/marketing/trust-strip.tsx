export function TrustStrip() {
  const sectionRef = useRef<HTMLElement>(null);
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true);
          observer.disconnect();
        }
      },
      { threshold: 0.2 }
    );

    if (sectionRef.current) {
      observer.observe(sectionRef.current);
    }

    return () => observer.disconnect();
  }, []);

  return (
    <section
      ref={sectionRef}
      className="relative bg-[var(--sand)]/30 px-5 py-12 md:px-8"
    >
      <div className="mx-auto max-w-6xl">
        <div
          className={`flex flex-wrap items-center justify-center gap-8 md:gap-16 transition-all duration-700 ${
            isVisible ? "opacity-100" : "opacity-0"
          }`}
        >
          {TRUST_ITEMS.map((item, index) => (
            <div
              key={item.label}
              className={`flex items-center gap-2 text-[var(--foreground-muted)] transition-all duration-700 ${
                isVisible ? "translate-y-0 opacity-100" : "translate-y-4 opacity-0"
              }`}
              style={{ transitionDelay: `${index * 100}ms` }}
            >
              {item.icon}
              <span className="text-sm font-medium">{item.label}</span>
              <MiniCrab className="ml-1 opacity-50" />
            </div>
          ))}
        </div>
      </div>
    </section>
  );
}
